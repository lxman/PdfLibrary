    // Scale a Q=50 base table to the requested quality (1..100).
    // Matches the IJG quality-to-scaling formula.
    public static ushort[] ScaleQuantTable(ushort[] base50, int quality)
    {
        if (quality < 1) quality = 1;
        if (quality > 100) quality = 100;
        int scale = quality < 50 ? 5000 / quality : 200 - 2 * quality;
        var result = new ushort[64];
        for (var i = 0; i < 64; i++)
        {
            int v = (base50[i] * scale + 50) / 100;
            if (v < 1) v = 1;
            if (v > 255) v = 255;
            result[i] = (ushort)v;
        }
        return result;
    }
